import esbuild from "esbuild";
import process from "process";

const builtins = [
	"assert", "buffer", "child_process", "crypto", "events", "fs", "http",
	"https", "net", "os", "path", "stream", "tls", "tty", "url", "util", "zlib",
];

const banner =
`/*
THIS IS A GENERATED/COMPILED FILE AND IS NOT MEANT TO BE EDITED.
*/
`;

const prod = (process.argv[2] === "production");

const context = await esbuild.context({
	banner: {
		js: banner,
	},
	entryPoints: ["main.ts"],
	bundle: true,
	external: [
		"obsidian",
		"electron",
		"@codemirror/autocomplete",
		"@codemirror/collab",
		"@codemirror/commands",
		"@codemirror/language",
		"@codemirror/lint",
		"@codemirror/search",
		"@codemirror/state",
		"@codemirror/view",
		"@lezer/common",
		"@lezer/highlight",
		"@lezer/lr",
		...builtins],
	format: "cjs",
	target: "es2018",
	logLevel: "info",
	sourcemap: prod ? false : "inline",
	treeShaking: true,
	outfile: "main.js",
});

if (prod) {
	await context.rebuild();
	process.exit(0);
} else {
	await context.watch();
}
